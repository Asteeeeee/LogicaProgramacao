// Duas expressoes resultar em TRUE (Aritmeticos / Relacionais / Logicos)
let verdadeira1 = 10 + 14 >= 10 && 200 + 150 == 350;
let verdadeira2 = 100 + 200 == 300 || 400 + 100 < 10;

console.log(verdadeira1);
console.log(verdadeira2);


